(* 練習問題5.1 *)
(* 1 *)
(* [[]] *)
(* 正しいリスト表現で 'a list list 型 *)

(* 2 *)
(* [[1; 3]; ["hoge"]] *)
(* 正しくないリスト表現．なぜなら要素の型があってないから．*)
(* 第一要素は int list 型で，第二要素は string list 型 *)

(* 3 *)
(* [3] :: [] *)
(* 正しいリスト表現 *)
(* [3] は int list 型なので，:: で結ぶなら右には int list list 型の要素である必要がある *)
(* ところが [] は 'a list 型であるから 'a として int list を選べば良い *)

(* 4 *)
(* 2 :: [3] :: [] *)
(* 正しくないリスト表現 *)
(* [3] :: [] は [[3]] となるので int list list 型だから，これに左から :: で結合できるのは int list 型 *)
(* ところが 2 は int 型である *)

(* 5 *)
(* [] :: [] *)
(* 正しいリスト表現 *)
(* 左の値は 'a list 型なので，右の値は 'a list list 型とすれば整合性が取れる *)

(* 6 *)
(* [(fun x -> x); (fun b -> not b)] *)
(* 正しいリスト表現 *)
